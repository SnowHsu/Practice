# 布林值練習Boolean bool (True/False) 
# 情境為對照Keelung and Taipei 的月下雨頻率
# 收穫1：布林值可以應用在比較的情境
monthly_average_rainning_frequency_at_Keelung = 0.25
monthly_average_rainning_frequency_at_Taipei = 0.15
print(monthly_average_rainning_frequency_at_Taipei == 0.25)
print(monthly_average_rainning_frequency_at_Taipei != 0.25)
print(monthly_average_rainning_frequency_at_Taipei > 0.25)
print(monthly_average_rainning_frequency_at_Taipei < 0.25)
print(monthly_average_rainning_frequency_at_Taipei >= 0.25)
print(monthly_average_rainning_frequency_at_Taipei <= 0.25)